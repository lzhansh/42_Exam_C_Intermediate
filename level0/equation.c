#include <stdio.h>

void    equation(int n)
{
	int A;
	int B;
	int C;

	A = -1;
	while (++A < 10)
	{
		B = -1;
		while (++B < 10)
		{
			C = -1;
			while (++C < 10)
			{
				if (A * 10 + B + C * 10 + A == n)
					printf("A = %d, B = %d, C = %d\n", A, B, C);
			}
		}
	}
}

